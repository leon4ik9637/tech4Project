package loops.forLoop;

public class PrintNumbersDescending {
    public static void main(String[] args) {

        /*
        Write a Java program to print numbers backward starting from 100 to 0
        (100 and 0 are included)

        start point: 100
        end point  : 0
        update     : decrement
         */
        for(int i = 100; i >= 0; i--){
            System.out.println(i);
        }


    }
}
