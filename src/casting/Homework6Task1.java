package casting;

public class Homework6Task1 {
    public static void main(String[] args) {

        String s1 = "5", s2 = "10";

        int s1Int = Integer.parseInt(s1), s2Int = Integer.parseInt(s2);

        System.out.println("Sum of 5 and 10 = " + (s1Int + s2Int));
        System.out.println("Product of 5 and 10 = " + (s1Int * s2Int));
        System.out.println("Division of 5 and 10 = " + (s1Int / s2Int));
        System.out.println("Subtraction of 5 and 10 = " + (s1Int - s2Int));
        System.out.println("Remainder of 5 and 10 = " + (s1Int % s2Int));


    }
}
